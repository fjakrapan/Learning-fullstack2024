let a = 10;
console.log(a);
console.log(a++); //ถ้าใส่ a++ จะเพิ่มค่า a หลังจากคำสั่งนี้ 
console.log(++a); //ถ้าใส่ ++a จะเพิ่มค่า a ก่อนคำสั่งนี้จะทำงาน
//แต่จะไม่มี ++a++ จะรันไม่ได้